const soma = (n1, n2) => {
    return Number(n1)+Number(n2)
}
const subtracao = (n1, n2) => {
    return Number(n1)-Number(n2)
}
const mutiplicacao = (n1, n2) => {
    return Number(n1)*Number(n2)
}
const divicao = (n1, n2) => {
    return Number(n1)/Number(n2)
}

export { soma, subtracao, mutiplicacao, divicao };
